    elseif pos(1) > 0.098522 && pos(2) > 0.8317 && ...
          pos(1) < 0.5751 && pos(2) < 0.8706
      set(Fig.main,'Pointer',pointer);
    else
      set(Fig.main,'Pointer','arrow');
    end

  end

  return

end

